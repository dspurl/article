<template>
	<view>
		<view v-if="data.length === 0">
			<view class="no-data flex justify-center">暂时没有文章哦~</view>
		</view>
		<block v-else>
			<view class="notice-item" v-for="(item, index) in data" :key="index" @click="goNavigator(item)">
				<text class="time">{{item.created_at}}</text>
				<view class="content">
					<block v-if="item.resources">
						<text class="title">{{item.name}}</text>
						<view class="img-wrapper">
							<image class="pic" :src="item.resources.img"></image>
						</view>
						<view class="bot b-t">
							<text>查看详情</text>
							<text class="more-icon yticon icon-you"></text>
						</view>
					</block>
					<block v-else>
						<view class="img-wrapper">
							<view class="cover">
								{{item.name}}
							</view>
						</view>
						<view class="bot b-t">
							<text>查看详情</text>
							<text class="more-icon yticon icon-you"></text>
						</view>
					</block>
				</view>
			</view>
			<uni-load-more :status="loadingType"></uni-load-more>
		</block>
	</view>
</template>

<script>
	import Article from '../../api/article'
	import uniLoadMore from '@/components/uni-load-more/uni-load-more.vue'
	export default {
		components: {
			uniLoadMore
		},
		data() {
			return {
				data: [],
				id: 0,
				page:1,
				loadingType: 'more'
			}
		},
		onLoad: function(options) {
			if(options.id){
				this.id = options.id
				uni.setNavigationBarTitle({
				    title: options.name
				})
				this.loadData()
			}
		},
		methods: {
			//获取列表
			loadData(type){
				const that = this
				Article.getList(that.id,{
					limit: 8,
					page: this.page
				},function(res){
					that.data = that.data.concat(res.data)
					if (res.last_page > that.page){
						that.page ++
						//判断是否还有数据， 有改为 more， 没有改为noMore
						that.loadingType = 'more'
					} else {
						that.loadingType = 'noMore'
					}
					if(type === 'pull'){
						setTimeout(function () {
							uni.stopPullDownRefresh();
						}, 1000)
					}
				})
			},
			goNavigator(item){
				uni.navigateTo({
					url: './details?list=0&id='+item.id
				})
			},
			onPullDownRefresh() {
				this.data = []
				this.page = 1
				this.loadData('pull')
			},
			onReachBottom(){
				if(this.loadingType !== 'noMore'){
					this.loadData()
				}
			}
		}
	}
</script>

